    public int trap(int[] height) {
        int  sum = 0 ;
        for (int i = 1; i < height.length-1; i++) {
            int l_max =0, r_max=0;
            for (int j = i; j >=0 ; j--) {
                l_max = Math.max(l_max,height[j]);
            }
            for (int j = i; j <height.length ; j++) {
                 r_max = Math.max(r_max,height[j]);
            }
            sum+= Math.min(l_max,r_max)- height[i];
        }
      return  sum;
    }



